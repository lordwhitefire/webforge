# ── Helpers ──
def read_package_json() -> dict:
    pkg_path = get_project_root() / "package.json"
    if not pkg_path.exists():
        return {}
    try:
        return json.loads(pkg_path.read_text())
    except Exception as _e:
        write_log("Docs", "Thoth", "parse package.json", {"error": str(_e)})
        return {}


def get_git_log(limit: int = 20) -> list:
    project = get_project_root()
    try:
        result = subprocess.run(
            ["git", "log", f"-{limit}", "--pretty=format:%H|%s|%an|%ad"],
            cwd=str(project), capture_output=True, text=True, timeout=10
        )
        if result.returncode != 0:
            return []
        logs = []
        for line in result.stdout.strip().split("\n"):
            parts = line.split("|", 3)
            if len(parts) == 4:
                logs.append({
                    "hash": parts[0][:8],
                    "message": parts[1],
                    "author": parts[2],
                    "date": parts[3],
                })
        return logs
    except Exception as _e:
        write_log("Docs", "Thoth", "get git log", {"error": str(_e)})
        return []


def count_files() -> dict:
    project = get_project_root()
    src = project / "src"
    stats = {"tsx": 0, "ts": 0, "test": 0, "total": 0}
    if src.exists():
        stats["tsx"] = len(list(src.rglob("*.tsx")))
        stats["ts"] = len(list(src.rglob("*.ts")))
        stats["test"] = len(list(src.rglob("*.test.*"))) + len(list(src.rglob("*.spec.*")))
    all_files = [f for f in project.rglob("*") if f.is_file()
                 and ".git" not in str(f) and "node_modules" not in str(f)
                 and ".next" not in str(f) and ".webforge" not in str(f)]
    stats["total"] = len(all_files)
    return stats


