# ── 2. Changelog ──
def changelog_generate() -> McpResult:
    """Generate changelog from git log + task board (Keep a Changelog format)."""
    logs = get_git_log(50)

    # Categorize commits using conventional commit format
    categories = {
        "Added": [],
        "Changed": [],
        "Deprecated": [],
        "Removed": [],
        "Fixed": [],
        "Security": [],
    }

    commit_map = {
        "feat": "Added",
        "add": "Added",
        "change": "Changed",
        "refactor": "Changed",
        "update": "Changed",
        "deprecate": "Deprecated",
        "remove": "Removed",
        "delete": "Removed",
        "fix": "Fixed",
        "bugfix": "Fixed",
        "hotfix": "Fixed",
        "security": "Security",
    }

    for log in logs:
        msg = log["message"].lower()
        categorized = False
        for prefix, category in commit_map.items():
            if msg.startswith(prefix + ":") or msg.startswith(prefix + "("):
                categories[category].append(log)
                categorized = True
                break
        if not categorized:
            # Try to guess from message content
            if "fix" in msg or "bug" in msg:
                categories["Fixed"].append(log)
            elif "add" in msg or "new" in msg or "create" in msg:
                categories["Added"].append(log)
            elif "change" in msg or "update" in msg or "refactor" in msg:
                categories["Changed"].append(log)
            elif "remove" in msg or "delete" in msg:
                categories["Removed"].append(log)
            else:
                categories["Changed"].append(log)

    content = """# Changelog

> Auto-generated by WebForge from git history.
> Format: [Keep a Changelog](https://keepachangelog.com/)

## [Unreleased]

"""
    for category, entries in categories.items():
        if entries:
            content += f"### {category}\n"
            for e in entries:
                content += f"- {e['message']} ({e['hash']})\n"
            content += "\n"

    content += f"""
---
*Generated by WebForge Documentation MCP on {utc_now()}*
*Based on last {len(logs)} commits.*
"""

    output = docs_output_dir() / "CHANGELOG.generated.md"
    output.write_text(content, encoding="utf-8")

    session_append("DOC GENERATED — Changelog", agent="Thoth", kind="note")
    return success({
        "file": str(output),
        "commits_analyzed": len(logs),
        "message": f"Changelog generated → {output}\n{len(logs)} commits analyzed.",
    })


# ── 3. API Documentation ──
def api_docs_generate() -> McpResult:
    """Generate API docs from scanning route files."""
    project = get_project_root()
    api_dir = project / "src" / "app" / "api"

    if not api_dir.exists():
        return fail("No API directory found (src/app/api/). This project may not have API routes.")

    routes = []
    for route_file in sorted(api_dir.rglob("route.ts")):
        rel_path = route_file.relative_to(project)
        # Extract route path from file path
        route_path = str(rel_path).replace("src/app/api", "").replace("/route.ts", "")
        if not route_path:
            route_path = "/"

        # Read the file to find HTTP methods and descriptions
        content = route_file.read_text(encoding="utf-8")

        methods = []
        for method in ["GET", "POST", "PUT", "PATCH", "DELETE"]:
            if f"export async function {method.lower()}" in content or f"export function {method.lower()}" in content:
                methods.append(method)

        # Try to find comments/descriptions
        desc_match = re.search(r'/\*\*(.*?)\*/', content, re.DOTALL)
        description = ""
        if desc_match:
            desc_lines = desc_match.group(1).strip().split("\n")
            description = " ".join(l.strip().lstrip("* ").strip() for l in desc_lines if l.strip() and not l.strip().startswith("@"))

        routes.append({
            "path": route_path,
            "file": str(rel_path),
            "methods": methods,
            "description": description.strip() or "(no description — add JSDoc comment)",
        })

    # Generate markdown
    content = """# API Documentation

> Auto-generated by WebForge from route files.
> Add JSDoc comments to your route files for better descriptions.

"""
    for r in routes:
        methods_str = " ".join(f"`{m}`" for m in r["methods"]) if r["methods"] else "(no methods exported)"
        content += f"## {methods_str} `{r['path']}`\n\n"
        content += f"**File:** `{r['file']}`\n\n"
        content += f"**Description:** {r['description']}\n\n"
        content += "---\n\n"

    content += f"\n*Generated by WebForge Documentation MCP on {utc_now()}*\n"
    content += f"*{len(routes)} routes found.*\n"

    output = docs_output_dir() / "API_DOCS.generated.md"
    output.write_text(content, encoding="utf-8")

    session_append(f"DOC GENERATED — API docs ({len(routes)} routes)", agent="Thoth", kind="note")
    return success({
        "file": str(output),
        "routes": len(routes),
        "message": f"API docs generated → {output}\n{len(routes)} routes documented.",
    })


# ── 4. Environment Documentation ──
def env_docs_generate() -> McpResult:
    """Document environment variables from .env.example + code usage."""
    project = get_project_root()
    env_example = project / ".env.example"

    if not env_example.exists():
        return fail("No .env.example file found. Create one first.")

    lines = env_example.read_text(encoding="utf-8").split("\n")

    vars_doc = []
    current_comment = ""

    for line in lines:
        line = line.strip()
        if not line:
            current_comment = ""
            continue
        if line.startswith("#"):
            current_comment = line.lstrip("# ").strip()
            continue
        if "=" in line:
            parts = line.split("=", 1)
            var_name = parts[0].strip()
            var_value = parts[1].strip() if len(parts) > 1 else ""

            # Determine if it's required
            is_required = not var_value or var_value == ""

            # Check if it's a public var (exposed to client)
            is_public = var_name.startswith("NEXT_PUBLIC_")

            vars_doc.append({
                "name": var_name,
                "example": var_value if var_value else "(required — no default)",
                "required": is_required,
                "public": is_public,
                "comment": current_comment,
            })
            current_comment = ""

    # Generate markdown
    content = """# Environment Variables

> Auto-generated by WebForge from .env.example

"""
    content += "| Variable | Required | Public | Description |\n"
    content += "|----------|----------|--------|-------------|\n"

    for v in vars_doc:
        req = "✅ yes" if v["required"] else "no"
        pub = "⚠️ yes" if v["public"] else "no"
        desc = v["comment"] or v["example"]
        content += f"| `{v['name']}` | {req} | {pub} | {desc} |\n"

    content += f"""
## Notes

- **Public** variables (NEXT_PUBLIC_*) are exposed to the browser. Never put secrets in them.
- **Required** variables must be set before the app will run.
- Copy `.env.example` to `.env.local` and fill in your values.

---
*Generated by WebForge Documentation MCP on {utc_now()}*
*{len(vars_doc)} variables found.*
"""

    output = docs_output_dir() / "ENV_DOCS.generated.md"
    output.write_text(content, encoding="utf-8")

    session_append(f"DOC GENERATED — Env docs ({len(vars_doc)} vars)", agent="Thoth", kind="note")
    return success({
        "file": str(output),
        "vars": len(vars_doc),
        "message": f"Env docs generated → {output}\n{len(vars_doc)} variables documented.",
    })


