# ── Generate all ──
def generate_all() -> McpResult:
    """Generate all 5 documentation types at once."""
    results = []

    print("Generating README...")
    r = readme_generate()
    results.append(("README", r))

    print("Generating Changelog...")
    r = changelog_generate()
    results.append(("Changelog", r))

    print("Generating API docs...")
    r = api_docs_generate()
    results.append(("API Docs", r))

    print("Generating Env docs...")
    r = env_docs_generate()
    results.append(("Env Docs", r))

    print("Generating Onboarding...")
    r = onboard_generate()
    results.append(("Onboarding", r))

    output_dir = docs_output_dir()
    lines = []
    lines.append("=" * 60)
    lines.append("DOCUMENTATION GENERATED")
    lines.append("=" * 60)
    lines.append(f"\nOutput directory: {output_dir}\n")

    for name, result in results:
        if result.ok:
            lines.append(f"  ✓ {name:15s} → {result.data.get('file', '').split('/')[-1]}")
        else:
            lines.append(f"  ✗ {name:15s} → {result.error}")

    lines.append("\nReview the generated files and merge into your project docs.")
    lines.append("Generated docs are in .webforge/docs/ (not tracked by git).")

    return success({"output": "\n".join(lines), "results": [(n, r.ok) for n, r in results]})


